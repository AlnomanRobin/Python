# Sclicing Strings
a="HelloAL, World!" # H=0,e=1,l=2
print(a[2:5]) # llo (2,3,4)

b="Hello, World!"
print(b[:5]) # 0,1,2,3,4 

# Slice To the End
c="Hello, World!"
print(c[2:]) # 0,1,2

d="Hello, World!"
print(d[-5:-2])

""" From: "o" in "World!" (position -5)
To, but not included: "d" in "World!" (position -2) """